    private void item_idKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_item_idKeyReleased
       
        ConnectionCalss c= new ConnectionCalss();
        try{
        con=c.getCon();
        String sql="Select itemName,Price,Discount,Category,Description,Pictures from additem where Item_id=?";
        PreparedStatement pst=con.prepareStatement(sql);
        pst.setString(1, item_id.getText());
        ResultSet rs=pst.executeQuery();
        if(rs.next()){
            
            name.setText(rs.getString("itemName"));
            price.setText(rs.getString("Price"));
            fdisc.setText(rs.getString("Discount"));
            catagory1.setText(rs.getString("Category"));
            description.setText(rs.getString("Description"));
            byte [] img=rs.getBytes("Pictures");
            ImageIcon imageicon=new ImageIcon(new ImageIcon(img).getImage().getScaledInstance(imagelable.getWidth(),imagelable.getHeight(),Image.SCALE_SMOOTH));
            imagelable.setIcon(imageicon);
            chooseimage=img;  
        }
        con.close();
        }
        catch(Exception e){
        
        }
        
        
    }//GEN-LAST:event_item_idKeyReleased

