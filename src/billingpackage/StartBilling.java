    private void addtocartActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addtocartActionPerformed

       
        ConnectionCalss c= new ConnectionCalss();
        try{
        con=c.getCon();
          String sql="INSERT INTO addtocart(Item_id,Name,Price,Discount,Quantity,Total_Price,Pictures) values(?,?,?,?,?,?,?)";
           PreparedStatement pst=con.prepareStatement(sql);
           
            pst.setString(1,item_id.getText());
            pst.setString(2,name.getText());
            pst.setDouble(3,Double.parseDouble(price.getText()));
            pst.setInt(4,Integer.parseInt(fdisc.getText()));
            pst.setInt(5,Integer.parseInt(number.getSelectedItem().toString()));
            
            pst.setDouble(6,Double.parseDouble(total.getText()));
            pst.setBytes(7,chooseimage);
            pst.executeUpdate();  
         JOptionPane.showMessageDialog(null,"insert successfully");
        con.close();
        }
        
        catch(Exception e){
            JOptionPane.showMessageDialog(null, e);
            System.out.println(e);
        
        }   
    }//GEN-LAST:event_addtocartActionPerformed

