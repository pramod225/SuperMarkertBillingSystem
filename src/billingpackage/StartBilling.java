    private void numberActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_numberActionPerformed
      Double price1=Double.parseDouble(price.getText());
      Integer dis=Integer.parseInt(fdisc.getText());
      Integer to=Integer.parseInt(number.getSelectedItem().toString());
      Double countdisc=(price1*dis/100);
      
      Double tprice=((price1-countdisc)*to);
      total.setText(tprice.toString());
        
    }//GEN-LAST:event_numberActionPerformed

