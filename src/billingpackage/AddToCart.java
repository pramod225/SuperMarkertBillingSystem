    private void addupdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addupdateActionPerformed
        ConnectionCalss c = new ConnectionCalss();
        Integer quantity = Integer.parseInt(number.getSelectedItem().toString());

        try {
            //System.out.println("Inside Update");
            con = c.getCon();
            int row = carttable.getSelectedRow();
            // System.out.println("Inside Update row = "+row);

            String value = (carttable.getModel().getValueAt(row, 0).toString());
            //System.out.println("Value is =  "+value);
            String priceSql = "Select Price,Discount from addtocart where Item_id='" + value + "'";
            PreparedStatement pst = con.prepareStatement(priceSql);
            ResultSet rs = pst.executeQuery();
            rs.next();
            double price = rs.getDouble("Price");
            int disc = rs.getInt("Discount");
            //System.out.println("Inside Update price = "+price);
            String sql = "Update addtocart set Quantity=?, Total_Price=? where Item_id='" + value + "'";
            pst = con.prepareStatement(sql);
            Double totalAmount = ((price * disc )/ 100);
            Double amount = ((price - totalAmount) * quantity);
            pst.setInt(1, quantity);
            pst.setDouble(2, amount);
            double sum=0;
            for(int i=0;i<carttable.getRowCount();i++){
                sum = sum + Double.parseDouble(carttable.getValueAt(i,5).toString());
            
            }
            
            lblprice.setText(Double.toString(sum));
            pst.executeUpdate();
            DefaultTableModel model = (DefaultTableModel) carttable.getModel();
            model.setRowCount(0);
            showcart();
            
            JOptionPane.showMessageDialog(null, "succeffuly");

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);

        }
        
        


    }//GEN-LAST:event_addupdateActionPerformed

