    public ArrayList<UserClass> userlist() {//display data in table 
        ArrayList<UserClass> userlist = new ArrayList<>();

        ConnectionCalss c = new ConnectionCalss();
        try {
            con = c.getCon();
            String query = "Select Item_id,Name,Price,Discount,Quantity,Total_Price,Pictures from addtocart";
            Statement st = con.createStatement();
            ResultSet rs = st.executeQuery(query);
            UserClass usertb;
            while (rs.next()) {
                usertb = new UserClass(rs.getString("Item_id"), rs.getString("Name"), rs.getDouble("Price"), rs.getInt("Discount"), rs.getInt("Quantity"), rs.getDouble("Total_Price"), rs.getBytes("Pictures"));
                userlist.add(usertb);

            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e);
            e.printStackTrace();
        }
        return userlist;
    }

    public void showcart() {
        ArrayList<UserClass> list = userlist();
        DefaultTableModel model = (DefaultTableModel) carttable.getModel();
        Object[] row = new Object[6];
        for (int i = 0; i < list.size(); i++) {
            row[0] = list.get(i).getitemid();
            row[1] = list.get(i).getname();
            row[2] = list.get(i).getprice();
            row[3] = list.get(i).getdiscount();
            row[4] = list.get(i).getquantity();
            row[5] = list.get(i).gettotalprice();
            model.addRow(row);

        }
    }
   


