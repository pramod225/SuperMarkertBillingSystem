    private void updateitemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateitemActionPerformed
       
         ConnectionCalss c = new ConnectionCalss();
         try {
             con=c.getCon();
             int row =edittable.getSelectedRow();
             System.out.println("value of row:"+row);
             String value =(edittable.getModel().getValueAt(row,0).toString());
             String sql="Update additem set Item_id=? ,ItemName=?, Price=?,Discount=?, Category=?, Description=?,Pictures=? Where Item_id ='"+value+"'";
             PreparedStatement pst=con.prepareStatement(sql);
             pst.setString(1,itemid.getText());
             pst.setString(2,itemname.getText());
             pst.setDouble(3,Double.parseDouble(price.getText()));
             pst.setInt(4,Integer.parseInt(editdisc.getText()));
             String items;
             items=selectitems.getSelectedItem().toString();
             pst.setString(5,items);
             pst.setString(6,itemsarea.getText());
             pst.setBytes(7,chooseimage);
             int x=pst.executeUpdate();
             DefaultTableModel model=(DefaultTableModel)edittable.getModel();
             model.setRowCount(0);
             showitems();
             JOptionPane.showMessageDialog(null,"successfully");
              System.out.println(x+"successfully");
                 
             con.close();
         }
         catch(Exception e){
         
             JOptionPane.showMessageDialog(this, e);
             System.out.println(e);
         
         }
         itemid.setText("");
        itemname.setText("");
        price.setText("");
        editdisc.setText("");
        selectitems.setSelectedIndex(0);
        itemsarea.setText("");
        imagelable.setText(null);
        
 
         
         
    }//GEN-LAST:event_updateitemActionPerformed

    private void edittableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_edittableMouseClicked
        int i =edittable.getSelectedRow();
        TableModel model=edittable.getModel();
        itemid.setText(model.getValueAt(i, 0).toString());
        itemname.setText(model.getValueAt(i, 1).toString());
        price.setText(model.getValueAt(i, 2).toString());
        editdisc.setText(model.getValueAt(i,3).toString());
 
        String selected=model.getValueAt(i, 4).toString();
        switch(selected){
            case "Grocery":
                selectitems.setSelectedIndex(1);
                break;
            case "Clothes":
                selectitems.setSelectedIndex(2);
                break;
            case "Shoes":
                selectitems.setSelectedIndex(3);
                break;
            case "Home and Kitchen":
                selectitems.setSelectedIndex(4);
                break;
            case "Household Items":
                selectitems.setSelectedIndex(5);
                break;
            case "Packed Food":
                selectitems.setSelectedIndex(6);
                break;
            case "Baverages":
                selectitems.setSelectedIndex(7);
                break;
        }
        itemsarea.setText(model.getValueAt(i, 5).toString());
        byte[] img=(userlist().get(i).getimage());//display image in table
        ImageIcon imageicon=new ImageIcon(new ImageIcon(img).getImage().getScaledInstance(imagelable.getWidth(),imagelable.getHeight(),Image.SCALE_SMOOTH));
        imagelable.setIcon(imageicon);
        chooseimage=img;
        
        
        
        
        
        
    }//GEN-LAST:event_edittableMouseClicked

