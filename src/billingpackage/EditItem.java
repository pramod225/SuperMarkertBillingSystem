    public ArrayList<UserClass>userlist(){//display data in table 
        ArrayList<UserClass>userlist=new ArrayList<>();
       
        ConnectionCalss c = new ConnectionCalss();       
        try {
        con=c.getCon();
        String query="Select Item_id,ItemName, Price,Discount,Category,Description,Pictures from additem";
        Statement st=con.createStatement();
        ResultSet rs=st.executeQuery(query);
        UserClass usertb;
        while(rs.next()){
            usertb=new UserClass(rs.getString("Item_id"),rs.getString("ItemName"),rs.getDouble("Price"),rs.getInt("Discount"),rs.getString("Category"),rs.getString("Description"),rs.getBytes("Pictures"));
            userlist.add(usertb);
        
        }
        
        
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(this, e);
        }
        return userlist;
    }
