    private void selectimageActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_selectimageActionPerformed
        JFileChooser choose=new JFileChooser();
      choose.showOpenDialog(null);
      File f =choose.getSelectedFile();
      filename=f.getAbsolutePath();
      ImageIcon imageicon=new ImageIcon(new ImageIcon(filename).getImage().getScaledInstance(imagelable.getWidth(),imagelable.getHeight(),Image.SCALE_SMOOTH));
      imagelable.setIcon(imageicon);
      try{
      File image=new File(filename);
      FileInputStream fis=new FileInputStream(image);
      ByteArrayOutputStream bos=new ByteArrayOutputStream();
      byte [] buf=new byte[1024];
      for(int readnum;(readnum=fis.read(buf))!=-1;){
       bos.write(buf,0,readnum);
      
      }
      chooseimage=bos.toByteArray();
      }
      catch(Exception e){
          JOptionPane.showMessageDialog(choose, e);
      
      }
        
      
    }//GEN-LAST:event_selectimageActionPerformed

