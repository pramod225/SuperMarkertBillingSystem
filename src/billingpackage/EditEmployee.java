    private void UpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_UpdateActionPerformed
      
        
         ConnectionCalss c = new ConnectionCalss();
         try {
         
         
             con=c.getCon();
             int row =table.getSelectedRow();
             String value =(table.getModel().getValueAt(row,0).toString());
             String sql="Update addemployee1 set  Emp_id=? , EmpName=?, Email=?, password=?, Phone_no=? Where Emp_id='"+value+"'";
             PreparedStatement pst=con.prepareStatement(sql);
             pst.setString(1,empid.getText());
             pst.setString(2,name.getText());
             pst.setString(3,email.getText());
             pst.setString(4,password.getText());
             pst.setInt(5,Integer.parseInt(phoneno.getText()));
             pst.executeUpdate();
             DefaultTableModel model=(DefaultTableModel)table.getModel();
             model.setRowCount(0);
             showuser();
             JOptionPane.showMessageDialog(null,"successfully");
                 
             con.close();
         }
         catch(Exception e){
         
             JOptionPane.showMessageDialog(null,e);
         System.out.println(e);
         }
         
         empid.setText("");
         name.setText("");
         email.setText("");
         password.setText("");
         phoneno.setText("");
         
         
        
    }//GEN-LAST:event_UpdateActionPerformed

