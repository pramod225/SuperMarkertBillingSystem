    public ArrayList<UserClass>userlist() {//display data in table 
        ArrayList<UserClass>userlist=new ArrayList<>();
       
        ConnectionCalss c=new ConnectionCalss();       
        try {
        con=c.getCon();
        String query="Select Emp_id,EmpName,Email,password,Phone_no from addemployee1";
        Statement st=con.createStatement();
        ResultSet rs=st.executeQuery(query);
        UserClass usertb;
        while(rs.next()){
            usertb=new UserClass(rs.getString("Emp_id"),rs.getString("EmpName"),rs.getString("Email"),rs.getString("password"),rs.getInt("Phone_no"));
            userlist.add(usertb);
        
        }
        
        }
        catch(Exception e){
        }
        return userlist;
    }
